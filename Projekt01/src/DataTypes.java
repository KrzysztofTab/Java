import java.sql.SQLOutput;

public class DataTypes {
    public static void main(String[] args) {
        System.out.print("Liczba int: ");
        System.out.print(25);
        System.out.print('\n');

        // unicode symbole
        System.out.print("Unicode: ");
        System.out.print('\u26F1');
        System.out.print('\n');

        System.out.print("Liczba double: ");
        System.out.print(25.75);
        System.out.print('\n');

        System.out.print("Znak A: ");
        System.out.print('A');
        System.out.print('\n');

            // typ logiczny boolean
        System.out.print("Czy uczę się programowania? ");
        System.out.println(true);
    }
}
