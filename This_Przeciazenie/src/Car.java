public class Car {
    int year;
    String brand;
    String model;
    String color;

    Car(int yearA, String brandA, String modelA, String colorA) {
        year = yearA;
        brand = brandA;
        model = modelA;
        color = colorA;
    }
    Car(int yearB, String brandB, String modelB){
        year = yearB;
        brand = brandB;
        model = modelB;
}

}
