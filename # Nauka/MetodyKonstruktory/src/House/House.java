package House;

public class House {
    double water;
    double fuel;

    public House(double water, double fuel) {
        this.water = water;
        this.fuel = fuel;
    }

    public double getWater() {
        return water;
    }

    public void setWater(double water) {
        this.water = water;
    }

    public double getFuel() {
        return fuel;
    }

    public void setFuel(double fuel) {
        this.fuel = fuel;
    }
}
