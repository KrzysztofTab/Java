package For;

public class NumberPrinter {
    public static void main(String[] args) {
        int[] numers = {1, 2, 3, 4, 5};
//        Pętla for each
//        tmp - dla każdego elementu tablicy numbers wykonaj polecenia w ciele pętli
        for (int tmp : numers) {
            System.out.println(tmp);
        }
    }
}
