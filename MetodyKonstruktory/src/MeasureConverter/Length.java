package MeasureConverter;

public class Length {

    void centymetry(int metry) {
        int result = metry * 100;
        System.out.println( "        = "+result +" cm.");
    }
    void milimetry(int metry) {
        int result = metry * 1000;
        System.out.println( "        = "+result +" mm.");
    }
}

//    1 metr to 100 centymetrów i 1000 milimetrów.